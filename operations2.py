def subtract(num_1,num_2):
    result = num_1 - num_2
    print(("%d + % d is equal to %d")%(num_1,num_2,result))
    return result

def multiply(num_1,num_2):
    result =(num_1 * num_2)
    print(("%d X %d is equal to %d")%(num_1,num_2,result))
    return result

def divide(num_1,num_2):
    result =(num_1/num_2)
    print(("%d dividided by %d is equal to %d")%(num_1,num_2,result))
    return result

def power(num_1,num_2):
    result =(num_1 ** num_2)
    print(("%d raised to the power of %d is equal to %d")%(num_1,num_2,result))
    return result

